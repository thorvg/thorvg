#include "Common.h"

/************************************************************************/
/* Drawing Commands                                                     */
/************************************************************************/

void tvgDrawCmds(tvg::Canvas* canvas)
{
    if (!canvas) return;

    auto opacity = 36;

    //Load jpg file from path
    for (int i = 0; i < 7; ++i) {
        auto picture = tvg::Picture::gen();
        if (picture->load(EXAMPLE_DIR"/test.jpg") != tvg::Result::Success) {
             cout << "JPG is not supported. Did you enable JPG Loader?" << endl;
             return;
        }
        picture->translate(i* 150, i * 150);
        picture->rotate(30 * i);
        picture->size(200, 200);
        picture->opacity(opacity + opacity * i);
        if (canvas->push(std::move(picture)) != tvg::Result::Success) return;
    }

    //Open file manually
    ifstream file(EXAMPLE_DIR"/test.jpg", ios::binary);
    if (!file.is_open()) return;
    auto begin = file.tellg();
    file.seekg(0, std::ios::end);
    auto size = file.tellg() - begin;
    auto data = (char*)malloc(size);
    if (!data) return;
    file.seekg(0, std::ios::beg);
    file.read(data, size);
    file.close();

    auto picture = tvg::Picture::gen();
    if (picture->load(data, size, "jpg", true) != tvg::Result::Success) {
        cout << "Couldn't load JPG file from data." << endl;
        return;
    }

    free(data);
    picture->translate(400, 0);
    picture->scale(0.8);
    canvas->push(std::move(picture));
}


/************************************************************************/
/* Sw Engine Test Code                                                  */
/************************************************************************/

static unique_ptr<tvg::SwCanvas> swCanvas;

void tvgSwTest(uint32_t* buffer)
{
    //Create a Canvas
    swCanvas = tvg::SwCanvas::gen();
    swCanvas->target(buffer, WIDTH, WIDTH, HEIGHT, tvg::SwCanvas::ARGB8888);

    /* Push the shape into the Canvas drawing list
       When this shape is into the canvas list, the shape could update & prepare
       internal data asynchronously for coming rendering.
       Canvas keeps this shape node unless user call canvas->clear() */
    tvgDrawCmds(swCanvas.get());
}

void drawSwView(void* data, Eo* obj)
{
    if (swCanvas->draw() == tvg::Result::Success) {
        swCanvas->sync();
    }
}


